package com.example.jufood;

import java.io.Serializable;
import java.util.ArrayList;

public class shopBot implements Serializable {



       String name;
       int price;
       int image;

    public shopBot(String name, int price, int image) {
        this.name = name;
        this.price = price;
        this.image = image;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getPrice() {
        return price;
    }

    public void setPrice(int price) {
        this.price = price;
    }

    public int getImage() {
        return image;
    }

    public void setImage(int image) {
        this.image = image;
    }
}
